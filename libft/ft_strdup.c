#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*str;
	int		i;
	size_t	s_len;

	s_len = ft_strlen(s);
	str = (char *)ft_calloc(s_len + 1, sizeof(char));
	if (!str)
		return (NULL);
	i = 0;
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	return (str);
}
